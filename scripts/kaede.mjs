async function cmdToday() {
  const secrets = getSecrets();
  if (!secrets.TRELLO_API_KEY || !secrets.TRELLO_TOKEN) {
    console.error('  \x1b[31m  ✗ Trello credentials not found.\x1b[0m');
    console.error('  \x1b[33m  Run `node scripts/kaede.mjs setup` first.\x1b[0m');
    return;
  }

  const { TRELLO_API_KEY, TRELLO_TOKEN } = secrets;
  const apiBase = 'https://api.trello.com/1';
  const auth = `key=${TRELLO_API_KEY}&token=${TRELLO_TOKEN}`;

  try {
    const meRes = await fetch(`${apiBase}/members/me?${auth}`);
    const me = await meRes.json();

    const boardsRes = await fetch(
      `${apiBase}/members/${me.id}/boards?${auth}&fields=name,id,url,closed&filter=open`
    );
    const boards = await boardsRes.json();

    console.log('');
    console.log(`  \x1b[35m╔══════════════════════════════════════╗\x1b[0m`);
    console.log(`  \x1b[35m║      KAEDE — Today's Tasks           ║\x1b[0m`);
    console.log(`  \x1b[35m╚══════════════════════════════════════╝\x1b[0m`);
    console.log(`  \x1b[90m  ${me.fullName || me.username} · ${new Date().toLocaleDateString('id-ID', { weekday: 'long', year: 'numeric', month: 'long', day: 'numeric' })}\x1b[0m`);
    console.log('');

    let hasTasks = false;

    for (const board of boards) {
      const listsRes = await fetch(
        `${apiBase}/boards/${board.id}/lists?${auth}&fields=name,id`
      );
      const lists = await listsRes.json();

      for (const list of lists) {
        const cardsRes = await fetch(
          `${apiBase}/lists/${list.id}/cards?${auth}&fields=name,id,due,dueComplete,idMembers,url,desc,dateLastActivity&members=true`
        );
        const cards = await cardsRes.json();

        const myCards = cards.filter((c) =>
          c.idMembers && c.idMembers.includes(me.id)
        );

        if (myCards.length === 0) continue;
        hasTasks = true;

        console.log(`  \x1b[36m  ${board.name}\x1b[0m \x1b[90m→\x1b[0m \x1b[33m${list.name}\x1b[0m`);
        console.log(`  \x1b[90m  ${'─'.repeat(Math.max(board.name.length + list.name.length + 4, 20))}\x1b[0m`);

        for (const card of myCards) {
          const due = card.due
            ? new Date(card.due).toLocaleDateString('id-ID', {
                day: 'numeric',
                month: 'short',
              })
            : '';
          const isOverdue =
            card.due && new Date(card.due) < new Date() && !card.dueComplete;
          const dueStr = due
            ? ` \x1b[90m[\x1b[0m${isOverdue ? '\x1b[31m⚠ ' : '\x1b[32m'}` +
              `${due}\x1b[0m${card.dueComplete ? ' ✓' : ''}\x1b[90m]\x1b[0m`
            : '';

          console.log(`    \x1b[37m• ${card.name}\x1b[0m${dueStr}`);
          if (card.desc) {
            const firstLine = card.desc.split('\n')[0].slice(0, 80);
            console.log(`     \x1b[90m${firstLine}\x1b[0m`);
          }
        }
        console.log('');
      }
    }

    if (!hasTasks) {
      console.log('  \x1b[90m  No tasks assigned to you. Enjoy your day! 🎉\x1b[0m');
      console.log('');
    }
  } catch (err) {
    console.error('  \x1b[31m  ✗ Failed to fetch Trello data:\x1b[0m', err.message);
  }
}

