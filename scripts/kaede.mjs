async function cmdRun() {
  const args = process.argv.slice(3);
  const getOpt = (f) => { const i = args.indexOf(f); return i !== -1 ? args[i + 1] : undefined; };
  const playbookPath = getOpt('--playbook');
  const isDryRun = args.includes('--dry-run') || args.includes('-n');

  // Known flags that take a value (skip both flag and value)
  const knownFlagArgs = ['--playbook', '--board', '--openkb', '--opencode'];
  // Extra args passed to intent handler (--name, --task, --list, --desc, --member, --color)
  const extraArgs = {};

  // Find intent and collect extra args
  let intent = '';
  for (let i = 0; i < args.length; i++) {
    if (args[i].startsWith('--') || args[i].startsWith('-')) {
      if (knownFlagArgs.includes(args[i])) {
        i++; // skip next arg (value)
        continue;
      }
      // Collect extra intent args: --name value --task value etc.
      const key = args[i].replace(/^--?/, '');
      if (i + 1 < args.length && !args[i + 1].startsWith('-')) {
        extraArgs[key] = args[i + 1];
        i++;
      } else {
        extraArgs[key] = true;
      }
      continue;
    }
    intent += (intent ? ' ' : '') + args[i];
  }

  if (!intent) {
    console.error('  \x1b[31m  вњ— Intent required.\x1b[0m');
    console.log('  \x1b[37m  Usage:\x1b[0m node scripts/kaede.mjs run \x1b[36m--playbook <path>\x1b[0m \x1b[90m"intent" [--name ...] [--task ...]\x1b[0m');
    console.log('  \x1b[37m  Example:\x1b[0m kaede run --playbook docs/playbook-template.md "buat board" --name "Sprint 1"');
    return;
  }

  console.log('');
  console.log('  \x1b[35mв•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—\x1b[0m');
  console.log('  \x1b[35mв•‘    KAEDE вЂ” Execute Intent            в•‘\x1b[0m');
  console.log('  \x1b[35mв•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ\x1b[0m');
  console.log('');
  console.log(`  \x1b[36m  Intent:\x1b[0m \x1b[90m"${intent}"\x1b[0m`);
  if (isDryRun) {
    console.log('  \x1b[33m  вљ   DRY RUN вЂ” no changes will be made.\x1b[0m');
  }

  if (playbookPath && existsSync(playbookPath)) {
    const { parsePlaybook, executeIntent } = await import(pathToFileURL(resolve(KAEDE_DIR, 'src', 'orchestrator.js')).href);
    const { TrelloMCPClient } = await import(pathToFileURL(resolve(KAEDE_DIR, 'src', 'trello-client.js')).href);

    const content = readFileSync(playbookPath, 'utf-8');
    const playbook = parsePlaybook(content);

    console.log(`  \x1b[36m  Playbook loaded:\x1b[0m \x1b[90m${playbook.workflow.lists.length} lists, ${playbook.roles.length} roles\x1b[0m`);

    if (isDryRun) {
      console.log('');
      console.log('  \x1b[36m  Would execute:\x1b[0m \x1b[90m"' + intent + '"\x1b[0m');
      console.log(`  \x1b[36m  Intents available:\x1b[0m \x1b[90mmulai sprint, buat card, assign, buat label, arsipkan, pindah semua, buat board, komentar, report, tutup sprint\x1b[0m`);
      console.log(`  \x1b[36m  Workflow lists:\x1b[0m \x1b[90m${playbook.workflow.lists.join(', ')}\x1b[0m`);
      console.log('');
      console.log('  \x1b[32m  вњ…  Dry run complete. Pass --board <id> to execute.\x1b[0m');
      console.log('');
      return;
    }

    console.log('');
    console.log('  \x1b[36m  Connecting to Trello MCP Server...\x1b[0m');

    const client = new TrelloMCPClient();
    try {
      await client.connect();
      console.log('  \x1b[32m  вњ“ Connected.\x1b[0m');
      console.log('');
      console.log('  \x1b[36m  Executing intent...\x1b[0m');

      let boardId = getOpt('--board');
      if (!boardId) {
        console.log('  \x1b[33m  вљ   No --board provided. Auto-detecting...\x1b[0m');
        try {
          const boards = await client.listBoards();
          if (boards.length === 0) {
            console.log('  \x1b[31m  вњ— No boards found. Cannot execute.\x1b[0m');
            return;
          }
          boardId = boards[0].id;
          console.log(`  \x1b[32m  вњ“ Using board: ${boards[0].name} (${boardId})\x1b[0m`);
        } catch (err) {
          console.log(`  \x1b[31m  вњ— Failed to auto-detect board: ${err.message}\x1b[0m`);
          return;
        }
      }
      const results = await executeIntent(client, intent, playbook, boardId, extraArgs);
      console.log('');
      for (const r of results) {
        if (r.success) {
          console.log(`  \x1b[32m  вњ“ ${r.type}: ${r.name}\x1b[0m`);
        } else {
          console.log(`  \x1b[31m  вњ— ${r.type}: ${r.name} вЂ” ${r.error}\x1b[0m`);
        }
      }

      console.log('');
      console.log('  \x1b[32m  \u2705  Intent execution completed.\x1b[0m');
    } catch (err) {
      console.error('  \x1b[31m  вњ— Execution failed:\x1b[0m', err.message);
    } finally {
      client.close();
    }
  } else {
    console.log('  \x1b[33m  вљ   Playbook not provided. Execute with generic defaults.\x1b[0m');
  }
  console.log('');
}

