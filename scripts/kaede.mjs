    if (listName) {
      targetList = lists.find((l) => l.name.toLowerCase().includes(listName.toLowerCase()));
      if (!targetList) {
        console.error('  \x1b[31m  вњ— List "' + listName + '" not found.\x1b[0m');
        console.error('  \x1b[90m  Available: ' + lists.map((l) => l.name).join(', ') + '\x1b[0m');
        return;
      }
    } else {
      targetList = lists.find((l) => !l.closed) || lists[0];
      console.log('  \x1b[90m  List: ' + targetList.name + ' (use --list to change)\x1b[0m');
    }

    console.log('');
    console.log('  \x1b[35mв•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—\x1b[0m');
    console.log('  \x1b[35mв•‘        KAEDE вЂ” Card Push             в•‘\x1b[0m');
    console.log('  \x1b[35mв•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ\x1b[0m');
