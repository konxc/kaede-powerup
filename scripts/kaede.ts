async function main(): Promise<void> {
  const cmd = process.argv[2] || 'help';

  switch (cmd) {
    case 'setup':
      await cmdSetup();
      break;
    case 'today':
      await cmdToday();
      break;
    case 'init':
      await cmdInit();
      break;
    case 'push':
      await cmdPush();
      break;
    case 'env':
      await cmdEnv();
      break;
    case 'build':
      await cmdBuild();
      break;
    case 'status':
      await cmdStatus();
      break;
    case 'playbook':
      await cmdPlaybook();
      break;
    case 'orchestrate':
      await cmdOrchestrate();
      break;
    case 'run':
      await cmdRun();
      break;
    case 'start':
      await cmdStart();
      break;
    case 'test-tools':
      await cmdTestTools();
      break;
    case 'install':
      await cmdInstall();
      break;
    case 'help':
    default:
      cmdHelp();
      break;
  }
  readline.close();
}

main().catch((err: Error) => {
  console.error('  \x1b[31m  ✗ Fatal:', err.message, '\x1b[0m');
  process.exit(1);
});
