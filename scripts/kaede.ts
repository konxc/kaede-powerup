interface EnvVars {
  TRELLO_API_KEY?: string;
  TRELLO_TOKEN?: string;
  [key: string]: string | undefined;
}

function loadEnv(filePath: string): EnvVars {
  if (!existsSync(filePath)) return {};
  const content = readFileSync(filePath, 'utf-8');
  const vars: EnvVars = {};
  for (const line of content.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eqIdx = trimmed.indexOf('=');
    if (eqIdx === -1) continue;
    const key = trimmed.slice(0, eqIdx).trim();
    const val = trimmed.slice(eqIdx + 1).trim();
    if (key) vars[key] = val;
  }
  return vars;
}

function getSecrets(): EnvVars {
  const global = resolve(homedir(), '.config', 'kaede', 'secrets.env');
  const local = resolve(process.cwd(), 'secrets.env');
  const dev = resolve(KAEDE_DIR, 'secrets.env');

  let merged: EnvVars = {};
  for (const p of [dev, local, global]) {
    merged = { ...merged, ...loadEnv(p) };
  }
  return merged;
}

let readline = createInterface({
  input: process.stdin,
  output: process.stdout,
});

function ask(query: string): Promise<string> {
  return new Promise((resolveFn) => readline.question(query, resolveFn));
}

